void DisplayGetDataToLCD(U8 x_page,U8 x_StringLine,U8 (*StringCode)[16],U8 x_Reverse)
{
    U8 i,x,y,k;
    U8 x_Col;

    x_Col=0;
    k=0;
    x_page = x_page*2;
    for(i=0;i<16;i++)
    {
       for(x=0;x<2;x++)
       {
          LCM_set_address(x_page+x,x_Col);
          for(y=0;y<8;y++)
          {
             if(x_Reverse)
             {
                LCM_write_data(~(CharIcon8x16_PlayMenu[StringCode[x_StringLine][i]][y+k])); 
             }
             else
             {
                LCM_write_data((CharIcon8x16_PlayMenu[StringCode[x_StringLine][i]][y+k]));  
             }
          }
          k +=8;
       }
       k=0;
       x_Col += 8;
    }
}
