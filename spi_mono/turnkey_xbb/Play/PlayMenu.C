// x_CurrentIndex==¶Ç¤J·í«eMENU¯Á¤Þ­È
// t_StringArray==¶Ç¤JMENU TABLE
//-------------------------------------------------------------------------------------------
void DisplayMenuScroll(U8 x_CurrentIndex,U8 x_MenuCnt,U8 (*t_StringArray)[16])
{
   U8 i,w_LcdLine,w_reverse,t_reverse;
   //------Åã¥ÜLCD-------------------------------------------------------------
   w_reverse=x_CurrentIndex%4;
   if(w_reverse)
     w_LcdLine=x_CurrentIndex-w_reverse;
   else
     w_LcdLine=x_CurrentIndex;

   for(i=0;i<4;i++)
   {
      if(i==w_reverse)
      {
         t_reverse=1;
      }else{
         t_reverse=0;
      }
      if(w_LcdLine<x_MenuCnt)
      {
         DisplayGetDataToLCD(i,w_LcdLine,t_StringArray,t_reverse);
      }else{
         LCD_erase_one_page_PlayMenu(i*2);
         LCD_erase_one_page_PlayMenu(i*2+1);
      }
      w_LcdLine++;
   }
}
