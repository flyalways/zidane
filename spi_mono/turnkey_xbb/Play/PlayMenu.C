void MenuModeSelect(void)
{
	switch(gc_KeyEvent)
	{
		case 0x03: //C_PlayNext//Down
			gc_MenuEndTimer=120;
			gc_MenuUpDownCnt++;
			if(gc_MenuUpDownCnt>=gc_MenuMaxValue)
			{
				gc_MenuUpDownCnt=0;
			}
			DisplayMenuFunction();
		break;

		case 0x04: //C_PlayPrev//Up
			gc_MenuEndTimer=120;
			if(gc_MenuUpDownCnt==0)
			{
				gc_MenuUpDownCnt=gc_MenuMaxValue-1;
			}
			else
			{
				gc_MenuUpDownCnt--;
			}
			DisplayMenuFunction();
		break;

		case 0x02: //-------C_PlayPause//Menu Enter--------------------------------------------
			if(gc_MenuLevel==0)
			{
				gc_MenuLevelFirst=gc_MenuUpDownCnt;
			}
			gc_MenuEndTimer=120;
			switch(gc_MenuLevel)
			{
				case 0x00://----------------------------------------------------------------
					gc_MenuLevel++;
					switch(gc_MenuUpDownCnt)
					{
						case 0x00:  //Language Mode  0~16
							gc_MenuMaxValue=17;
						break;

						case 0x01:  //Play Mode 0~3
							gc_MenuMaxValue=4;
						break;

						case 0x02:  //EQ Mode  0~6
							gc_MenuMaxValue=7;
						break;

						case 0x03:  //Exit Menu
							MenuDisplayExit();
						return;
                    }
					gc_MenuUpDownCnt=0;
					DisplayMenuFunction();
				break;

				case 0x01: //Success Save Data-----------------------------------------------
					switch(gc_MenuLevelFirst)
					{
						case 0x00:   //Language 0~16
							if(gc_MenuUpDownCnt<16)
							{
								gc_CurrentHZK=gc_MenuUpDownCnt;
								MenuDisplayExit();
							}
							else
							{
								if(gc_MenuUpDownCnt==16)
								{
									DisplayFirstMenu();
								}
								else
								{
									MenuDisplayExit();
								}
							}
						break;

						case 0x01://Repeat Mode 0~3-----------------------------------------
							if(gc_MenuUpDownCnt<3)//EQ ªì©l­È¨S¦³³]©w¬°0
							{
								gc_RepPlayMode=gc_MenuUpDownCnt+1;
								MenuDisplayExit();
							}
							else
							{
								if(gc_MenuUpDownCnt==3)
								{
									DisplayFirstMenu();
								}
								else
								{
									MenuDisplayExit();
								}
							}
						break;

						case 0x02://EQ Mode 0~6---------------------------------------------
							if(gc_MenuUpDownCnt<6)
							{
								gs_DSP_GLOBAL_RAM.sc_EQ_Type=gc_MenuUpDownCnt;
								Music_EQ_Cmd();
								MenuDisplayExit();
							}
							else
							{
								if(gc_MenuUpDownCnt==6)
								{
									DisplayFirstMenu();
								}
								else
								{
									MenuDisplayExit();
								}
							}
						break;
					}
				break;
			}
		break;
	}
}



